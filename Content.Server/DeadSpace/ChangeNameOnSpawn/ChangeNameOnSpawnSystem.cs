    public override void Initialize()
    {
        SubscribeLocalEvent<ChangeNameOnSpawnComponent, PlayerSpawnCompleteEvent>(OnPlayerSpawned);
    }

    private void OnPlayerSpawned(EntityUid ent, ChangeNameOnSpawnComponent component, PlayerSpawnCompleteEvent args)
    {
        ChangeName(ent, component, args);
    }

    private void ChangeName(EntityUid ent, ChangeNameOnSpawnComponent component, PlayerSpawnCompleteEvent args)
    {
        var job = args.JobId;
        string name;

        if (job == "Mime")
            name = _random.Pick(_mimeNames);
        else
            name = _random.Pick(_clownNames);

        _metaSystem.SetEntityName(args.Mob, name);
    }
